class Arithmetic{

  double add({double firstNumber = 0, double secondNumber = 0,})=>
     firstNumber + secondNumber;

  double substract( {double firstNumber = 0, double secondNumber = 0,})=>
     firstNumber-secondNumber;

  double multiply({double firstNumber = 0, double secondNumber = 0,})=>
     firstNumber*secondNumber;

  double divide({double firstNumber = 0, double secondNumber = 0,})=>
     firstNumber/secondNumber;




}